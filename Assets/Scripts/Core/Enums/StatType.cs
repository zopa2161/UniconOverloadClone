namespace Core.Enums
{
    public enum StatType
    {
        // ==========================================
        // 1. 핵심 자원 (Core Pools)
        // ==========================================
        MaxHP,
        HP, // 최대 체력
        AP, // 액티브 포인트 (내 턴에 소모하는 행동력)
        PP, // 패시브 포인트 (조건부/반격 시 소모하는 행동력)

        // ==========================================
        // 2. 공격 능력치 (Offensive Stats)
        // ==========================================
        PhysicalAttack, // 물리 공격력 (P.ATK)
        MagicalAttack, // 마법 공격력 (M.ATK)
        Accuracy, // 명중률 (기본 100 기준, 회피율과 차감 연산)
        CriticalRate, // 치명타 확률 (%)
        CriticalDamage, // 치명타 피해 배율 (기본 1.5배 등)

        // ==========================================
        // 3. 방어 능력치 (Defensive Stats)
        // ==========================================
        PhysicalDefense, // 물리 방어력 (P.DEF)
        MagicalDefense, // 마법 방어력 (M.DEF)
        Evasion, // 회피율 (%)
        GuardRate, // 가드 발동 확률 (방패 방어 등, %)
        GuardEfficiency, // 가드 성공 시 피해 감소율 (%)

        // ==========================================
        // 4. 유틸리티 (Utility Stats)
        // ==========================================
        Initiative, // 행동 속도 (TurnManager에서 순서를 결정하는 기준)
        Movement // 이동력 (그리드 전투 시 이동 가능 칸 수, 필요 시 사용)
    }
}